    private void fillTable(ArrayList<NhanVien> list) {
        defaultTableModel = (DefaultTableModel) tblNhanVien.getModel();
        defaultTableModel.setRowCount(0);
        for (NhanVien nhanVien : list) {
            defaultTableModel.addRow(new Object[]{
                nhanVien.getMaNV(), nhanVien.getHoTen(), nhanVien.getPhongBan(), nhanVien.getTuoi(),
                nhanVien.getDiaChi(), nhanVien.hienThiChucVu()
            });
        }
    }
