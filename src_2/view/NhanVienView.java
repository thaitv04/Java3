    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        // TODO add your handling code here:
        int index = tblNhanVien.getSelectedRow();
        txtID.setText(tblNhanVien.getValueAt(index, 0).toString());
        txtHoTen.setText(tblNhanVien.getValueAt(index, 1).toString());
        choPhongBan.setSelectedItem(tblNhanVien.getValueAt(index, 2).toString());
        txtTuoi.setText(tblNhanVien.getValueAt(index, 3).toString());
        txtDiaChi.setText(tblNhanVien.getValueAt(index, 4).toString());
        if (tblNhanVien.getValueAt(index, 5).equals("Truong phong")) {
            rdTruongPhong.setSelected(true);
        } else {
            rdNhanVien.setSelected(true);
        }
    }//GEN-LAST:event_tblNhanVienMouseClicked

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        Integer maNV  = Integer.parseInt(txtID.getText());
        String hoTen = txtHoTen.getText();
        String phongBan = choPhongBan.getSelectedItem().toString();
        Integer tuoi = Integer.parseInt(txtTuoi.getText());
        String diaChi = txtDiaChi.getText();
        Integer chucVu;
        if(rdTruongPhong.isSelected()){
            chucVu = 1;
        } else {
            chucVu = 0;
        }
        NhanVien nhanVien = new NhanVien(maNV, hoTen, phongBan, tuoi, diaChi, chucVu);
        String kq = nhanVienService.them(nhanVien);
        JOptionPane.showMessageDialog(this, kq);
        fillTable(nhanVienService.getList());
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblNhanVien.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(this, "Chọn dòng cần xóa");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Có chắc muốn xóa không");
        if(confirm != JOptionPane.YES_OPTION){
            return;
        }
        Integer maNV = Integer.parseInt(txtID.getText());
        NhanVien nhanVien = new NhanVien();
        JOptionPane.showMessageDialog(this, nhanVienService.xoa(maNV, nhanVien));
        fillTable(nhanVienService.getList());
    }//GEN-LAST:event_btnDeleteActionPerformed

